#import "SuitDetailEntity.h"

@implementation SuitDetailEntity

- (void)dealloc
{
    self.originalprice = nil;//原始价格
    self.discount = nil;//折扣
    self.suitprice = nil;//套餐价格
    
    self.suits = nil;//套装商品对象数组
    
    [super dealloc];
}
@end

@implementation SuitEntity

- (void)dealloc
{
    self.goodsid = nil;
    self.img = nil;//套装商品图片下载地址
    self.name = nil;//套装商品名称
    self.price = nil;//套装商品价格
    
    self.size = nil;//套装商品尺寸数组stringArray
    self.color = nil;//套装商品颜色数组stringArray
    
    [super dealloc];
}

@end
