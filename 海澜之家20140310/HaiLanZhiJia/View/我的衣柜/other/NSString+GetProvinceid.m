#import "NSString+GetProvinceid.h"

@implementation NSString (GetProvinceid)

- (int)getProvinceid
{
    NSMutableDictionary *dic = [[NSUserDefaults standardUserDefaults]objectForKey:KeyForProvinceID];
    return [[dic objectForKey:self]intValue];
}

@end
